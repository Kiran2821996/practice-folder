function change1() {
  document.querySelector(".p1").classList.toggle("red");
  if (document.querySelector(".p1").innerText == "Hai iam Black") {
    document.querySelector(".p1").innerText = "Hai iam red";
  } else {
    document.querySelector(".p1").innerText = "Hai iam Black";
  }
}
