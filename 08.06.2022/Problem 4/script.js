// Take an integer and return "YES" if the input is divisible by 3 or 5 or both. "NO" in all other cases

// Constraints:
// 0 <= input <= 1000

let a = parseInt(prompt("Enter an Integer"));


if(a%3==0||a%5==0){
    window.alert("YES-the input is divisible by 3 or 5 or both");}
    else{
        window.alert("NO-the input is not divisible by 3 or 5 or both");}